    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    try{
            if(validateInput()==false){
                JOptionPane.showMessageDialog(null,"please fill all the fields","Empty Values!!",JOptionPane.ERROR_MESSAGE);
            }
            
           
            boolean status=EmpDao.removeEmp(str);
            if(status)
            {
                JOptionPane.showMessageDialog(null," employee removed successfully! ","Success!",JOptionPane.INFORMATION_MESSAGE);
                model.setRowCount(0);
                txtEmpId.setText("");
            }
            else
                JOptionPane.showMessageDialog(null," employee not removed!","Updation Denied!",JOptionPane.ERROR_MESSAGE);
            
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error loading Driver class"+ex,"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

