    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
      try{
            
            if(txtEmpId.getText().isEmpty())
            return;
            EmpId=txtEmpId.getText();
            empList=EmpDao.getEmpById(EmpId);
             if(empList.isEmpty()==true)
                JOptionPane.showMessageDialog(null,"Sorry! No employee present","Error!",JOptionPane.ERROR_MESSAGE);
            else
            {
                Object[]rows=new Object[4]; //size corresponds to the no. of columns in the table
                
                        model=(DefaultTableModel)jtEmpDetails.getModel();//return type of getmodel() is TableModel , thats why we down cast it to its base class DefaultTableModel
                for(Emp e:empList.keySet()){
                    rows[0]=e.getEmpId();
                    rows[1]=e.getEmpName();
                    rows[2]=e.getJob();
                    rows[3]=e.getSalary();
                    
                    model.addRow(rows);
                }
            }
                
        }
        catch(Exception ex){
            
        ex.printStackTrace();
    }  // TODO add your handling code here:
    }//GEN-LAST:event_btnSearchActionPerformed

